COMMENT ON TABLE public.invoices IS
$$One row per invoice issued by the Taxi company to a Payer.
Covers three billing modes: monthly runs, single trips, or
per-Fahrgast billing. Follows the German invoice status lifecycle:
  draft → sent → paid | cancelled (Storno)

Financial totals are stored as an immutable snapshot at the time
of finalization. All detail rows live in invoice_line_items.$$;

COMMENT ON COLUMN public.invoices.id IS
$$Primary key (UUID). Auto-generated on insert.$$;

COMMENT ON COLUMN public.invoices.company_id IS
$$FK → companies.id (UUID). Used for multi-tenant data isolation.
Every query on invoices must filter by company_id to ensure
one company cannot access another company's invoices.
Type is UUID (not TEXT) — must match companies.id exactly.$$;

COMMENT ON COLUMN public.invoices.invoice_number IS
$$Human-readable invoice number. Format: RE-YYYY-NNNN (e.g. RE-2026-0001).
Must be globally unique and uninterrupted (§14 UStG).
Generated by: src/features/invoices/lib/invoice-number.ts.
For Stornorechungen the format is: STORNO-RE-YYYY-NNNN.$$;

COMMENT ON COLUMN public.invoices.payer_id IS
$$FK → payers.id. The Kostenträger (Leistungsempfänger) this
invoice is issued to. Payer address for the invoice PDF is read
from payers.street / city etc. at PDF render time (not snapshotted).$$;

COMMENT ON COLUMN public.invoices.billing_type_id IS
$$FK → billing_types.id. Optional filter to one billing family
(Abrechnungsfamilie) within the payer.
NULL = all billing types for this payer are included in the invoice.$$;

COMMENT ON COLUMN public.invoices.mode IS
$$Billing mode. Controls which trips are collected:
  monthly     → all trips for the payer in period_from..period_to
  single_trip → exactly one trip (referenced in invoice_line_items.trip_id)
  per_client  → all trips for one Fahrgast in the period (client_id required)

Allowed values enforced by CHECK constraint.$$;

COMMENT ON COLUMN public.invoices.client_id IS
$$FK → clients.id. The specific Fahrgast for per_client billing mode.
In this mode the client is the invoice recipient (not the payer).
NULL for monthly and single_trip modes.$$;

COMMENT ON COLUMN public.invoices.period_from IS
$$Start date (inclusive) of the billing period.
For monthly: first day of the month. For single_trip: the trip date.$$;

COMMENT ON COLUMN public.invoices.period_to IS
$$End date (inclusive) of the billing period.
For monthly: last day of the month. For single_trip: the trip date.$$;

COMMENT ON COLUMN public.invoices.status IS
$$Current lifecycle status. Allowed values (enforced by CHECK constraint):
  draft      – being prepared, can still be edited
  sent       – sent to the payer, amounts locked
  paid       – payment received
  cancelled  – storniert; a Stornorechnung was auto-created
  corrected  – set on the original invoice when it has been replaced by a Storno

Transitions:
  draft → sent → paid
  sent  → cancelled  (auto-creates Stornorechnung)$$;

COMMENT ON COLUMN public.invoices.subtotal IS
$$Nettobetrag — total before tax. Immutable snapshot, set when invoice is finalized.
Sum of all invoice_line_items.total_price.$$;

COMMENT ON COLUMN public.invoices.tax_amount IS
$$Total MwSt (Steuerbetrag). Immutable snapshot.
May include both 7% and 19% portions (see invoice_line_items.tax_rate).$$;

COMMENT ON COLUMN public.invoices.total IS
$$Bruttobetrag (Endbetrag) — subtotal + tax_amount. Immutable snapshot.$$;

COMMENT ON COLUMN public.invoices.notes IS
$$Optional free-text note printed in the invoice body.
Example: "Gemäß Rahmenvertrag Nr. 1234 vom 01.01.2026".$$;

COMMENT ON COLUMN public.invoices.payment_due_days IS
$$Days from invoice date until payment is due (Zahlungsziel).
Copied from company_profiles.default_payment_days on creation.
Editable per invoice in the invoice builder.$$;

COMMENT ON COLUMN public.invoices.created_by IS
$$auth.uid() of the admin who created this invoice.
For audit trail: traces invoice creation back to a specific user.$$;

COMMENT ON COLUMN public.invoices.created_at IS
$$Timestamp (UTC) when the invoice was first created (as draft).$$;

COMMENT ON COLUMN public.invoices.updated_at IS
$$Timestamp (UTC) of the last modification (status change or draft edit).$$;

COMMENT ON COLUMN public.invoices.sent_at IS
$$Timestamp (UTC) when the invoice was marked as sent. NULL if draft.$$;

COMMENT ON COLUMN public.invoices.paid_at IS
$$Timestamp (UTC) when payment was received. NULL if not yet paid.$$;

COMMENT ON COLUMN public.invoices.cancelled_at IS
$$Timestamp (UTC) when the invoice was storniert. NULL if not cancelled.$$;

COMMENT ON COLUMN public.invoices.cancels_invoice_id IS
$$Self-referencing FK → invoices.id.
Set on a Stornorechnung to point to the original invoice it cancels.
NULL on all normal invoices.

To find the Stornorechnung for an invoice:
